#!/usr/bin/env ferrum
/**
 * Hello World Example
 *
 * This is a simple example that demonstrates basic JavaScript execution in Ferrum.
 */

// Basic console output
console.log("Hello, Ferrum!");

// String interpolation
const name = "World";
console.log(`Hello, ${name}!`);

// Using variables
const greeting = "Welcome to Ferrum!";
console.log(greeting);

// Numbers and math
const result = 2 + 2;
console.log("2 + 2 =", result);

// Arrays
const fruits = ["apple", "banana", "cherry"];
console.log("Fruits:", fruits);

// Objects
const person = {
  name: "Ferrum",
  version: "0.1.0",
  features: ["Fast", "Secure", "Modern"]
};
console.log("Person:", person);
